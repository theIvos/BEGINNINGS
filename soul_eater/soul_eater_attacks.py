def learn(player,item,form_nr):
    for i in learn_attacks:
        if item["value"] == i["name"]:
            if i not in player.forms[form_nr]["attack_list"]:
                player.forms[form_nr]["attack_list"].append(i)
                if player.forms[form_nr]["name"] == "NO FORM":
                    print(f"{player.user_name} have learned {i['name']}!")
                else:
                    print(f"{player.forms[form_nr]['name']} have learned {i['name']}!")
            else:
                if player.forms[form_nr]["name"] == "NO FORM":
                    print(f"{player.user_name} already knows {i['name']}.")
                else:
                    print(f"{player.forms[form_nr]['name']} already knows {i['name']}.")



def heal(player,item,form_nr):
    og_current_hp = player.forms[form_nr]["current_hp"]
    overtop = "NO"
    player.forms[form_nr]["current_hp"] += item["value"]
    if player.forms[form_nr]["current_hp"] > player.forms[form_nr]["hp"]:
        overtop = "YES"
        player.forms[form_nr]["current_hp"] = player.forms[form_nr]["hp"]

    if overtop == "YES":
        if player.forms[form_nr]["name"] == "NO FORM":
            print(f"{player.user_name} was healed by {player.forms[form_nr]['hp'] - og_current_hp} HP points!")
        else:
            print(f"{player.forms[form_nr]['name']} was healed by {player.forms[form_nr]['hp'] - og_current_hp} HP points!")

    else:
        if player.forms[form_nr]["name"] == "NO FORM":
            print(f"{player.user_name} was healed by 100 HP points!")
        else:
            print(f"{player.forms[form_nr]['name']} was healed by 100 HP points!")


